//Guards:

// In NestJS, guards are a powerful feature that allows you to handle incoming requests before they reach the route handler. Guards can be used for authentication, authorization, validation. They help in controlling whether a request should be processed further or not based on certain conditions.

// Authentication Guards: These guards are used to check whether a user is authenticated or not. They are commonly used to protect routes that require authentication. If a request is not authenticated, the guard can redirect the user to a login page or return an error response.

// Authorization Guards: These guards are used to check whether a user is authorized to access a specific resource or perform a certain action. They are used to implement role-based access control or other authorization mechanisms.

// Execution Context Guards: These guards are used to perform validation on the incoming request data or headers. They can reject requests that don't meet the required criteria.

